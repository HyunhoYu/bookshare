-- After creation, find sequence name:
--   SELECT DATA_DEFAULT FROM USER_TAB_IDENTITY_COLS WHERE TABLE_NAME = 'POST_LIKE';

CREATE TABLE POST_LIKE (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    POST_ID NUMBER NOT NULL,
    USER_ID NUMBER NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT FK_PL_POST FOREIGN KEY (POST_ID) REFERENCES BOOK_OWNER_POST(ID),
    CONSTRAINT FK_PL_USER FOREIGN KEY (USER_ID) REFERENCES USERS(ID),
    CONSTRAINT UQ_POST_LIKE_PAIR UNIQUE (POST_ID, USER_ID)
);

CREATE INDEX IDX_PL_POST_ID ON POST_LIKE(POST_ID);


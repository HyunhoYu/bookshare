-- WISHLIST table for customer book favorites (#10)
CREATE TABLE WISHLIST (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    CUSTOMER_ID NUMBER NOT NULL,
    BOOK_ID NUMBER NOT NULL,
    CREATED_AT TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_WISHLIST_CUSTOMER FOREIGN KEY (CUSTOMER_ID) REFERENCES USERS(ID),
    CONSTRAINT FK_WISHLIST_BOOK FOREIGN KEY (BOOK_ID) REFERENCES BOOK(ID),
    CONSTRAINT UQ_WISHLIST UNIQUE (CUSTOMER_ID, BOOK_ID)
);

CREATE TABLE PURCHASE_REVIEW (
    ID NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    BOOK_SALE_RECORD_ID NUMBER NOT NULL,
    CUSTOMER_ID NUMBER NOT NULL,
    BOOK_OWNER_ID NUMBER NOT NULL,
    RATING NUMBER(1) NOT NULL CHECK (RATING BETWEEN 1 AND 5),
    CONTENT VARCHAR2(1000),
    CREATED_AT TIMESTAMP DEFAULT SYSTIMESTAMP NOT NULL,
    UPDATED_AT TIMESTAMP,
    CONSTRAINT FK_PR_SALE FOREIGN KEY (BOOK_SALE_RECORD_ID) REFERENCES BOOK_SALE_RECORD(ID),
    CONSTRAINT FK_PR_CUSTOMER FOREIGN KEY (CUSTOMER_ID) REFERENCES USERS(ID),
    CONSTRAINT FK_PR_OWNER FOREIGN KEY (BOOK_OWNER_ID) REFERENCES USERS(ID),
    CONSTRAINT UQ_PR_SALE_RECORD UNIQUE (BOOK_SALE_RECORD_ID)
);

CREATE INDEX IDX_PR_CUSTOMER ON PURCHASE_REVIEW(CUSTOMER_ID);
CREATE INDEX IDX_PR_OWNER ON PURCHASE_REVIEW(BOOK_OWNER_ID);
